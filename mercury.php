                            <p>Because the planet is so close to the sun, Mercury's surface temperature can reach a scorching 840 degrees Fahrenheit (450 degrees Celsius). However, since this world doesn't have a real atmosphere to entrap any heat, at night temperatures can plummet to minus 275 F (minus 170 C), a temperature swing of more than 1,100 degrees F (600 degree C), the greatest in the solar system.</p>
                            <p>Mercury is the smallest planet — it is only slightly larger than Earth's moon. Since it has no significant atmosphere to stop impacts, the planet is pockmarked with craters.</p>
                            <p>Mercury apparently shrank as much as 4.4 miles (7 km) as it cooled in the billions of years after its birth. This caused its surface to crumple, creating lobe-shaped scarps or cliffs, some hundreds of miles long and soaring up to a mile high. At the same time, the surface was constantly reshaped by volcanic activity in the planet’s past.</p>
