                            <ol class="rounded-list">
                                <li><a >Mercury is the smallest planet in our solar system.</a></li>
                                <li><a >It is about the size of the continental United States, only slightly bigger than Earth's moon.</a></li>
                                <li><a >One day on Mercury takes 59 Earth days, but one year on Mercury makes is only 88 Earth days long.</a></li>
                                <li><a >Mercury's thin atmosphere is composed mostly of oxygen (O2), sodium (Na), hydrogen (H2), helium (He), and potassium (K), the thinnest of any planet.</a></li>
                                <li><a >Mercury has no moons.</a></li>
                                <li><a >No evidence for life has been found on Mercury. It is unlikely life (as we know it) could survive on this planet.</a></li>
                                <li><a >Standing on Mercury's surface at its closest point to the sun, the sun would appear more than three times larger than it does on Earth.</a></li>
                                <li><a >Scientists think that there is ice inside Mercury's craters. The planet's north and south poles are cold and shadowy, which could allow them to retain water ice. </a></li>
                                <li><a >Due to its small mass, If you weigh 100 pounds on Earth, you would only weigh 37 pounds on Mercury.</a></li>
                                <li><a >Since Mercury’s orbit is within Earth’s orbit, it can be viewed from Earth in the early morning or the late evening, but never in the middle of the night.</a></li>
