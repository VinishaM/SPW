                            <div class='col-md-12 inner2' align='left'>
                                <p>The first spacecraft to visit Mercury was Mariner 10, which imaged about 45 percent of the surface and detected its magnetic field. NASA's MESSENGER orbiter is the second spacecraft to visit Mercury. When it arrived in March 2011, MESSENGER became the first spacecraft to orbit Mercury, where it continues to study the planet. [First Photos of Mercury from Orbit]</p>
                                
                                <p>In 2012, scientists discovered a group of meteorites in Morocco that they think could have originated from the planet Mercury. If so, it would make the rocky planet a member of a very select club with samples available on Earth; only the moon, Mars and the asteroid belt have verified rocks.</p>
