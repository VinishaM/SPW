                            <h2 class="headers" style="font-size: 1.4em;" >Basic Information</h2>
                            <div class='col-sm-12 blah inner2'>
                                <p>Venus, a world of intense heat and volcanic activity, is very similar to Earth in structure and size. It also has a thick and toxic atmosphere, trapping heat as a result of a <a href='http://www.nasa.gov/centers/ames/news/releases/2002/02_60AR.html'><b>runaway greenhouse</b></a> effect. The planet's heat is so intense that it's temperatures could melt lead. Interestingly, Venus also spins slowly in the opposite direction of most planets.</p>
