                                <ol class="rounded-list">
                                    <li><a >Venus is only slighly smaller than planet Earth.</a></li>
                                    <li><a >Venus is the second closest planet to the Sun.</a></li>
                                    <li><a >One day on Venus lasts as long as 243 Earth days, and makes a complete orbit around the Sun in 225 Earth days.</a></li>
                                    <li><a >Venus is a rocky, or "terrestrial" planet. Its solid surface is a cratered and volcanic landscape with more volcanoes than any other planet.</a></li>
                                    <li><a >Venus's atmosphere is made up mostly of carbon dioxide (CO2) and nitrogen (N2), with clouds of sulfuric acid (H2SO4) droplets.</a></li>
                                    <li><a >Venus has no moons and no rings.</a></li>
                                    <li><a >More than 40 spacecraft have explored Venus. The Magellan mission in the early 1990s mapped 98 percent of the planet's surface.</a></li>
                                    <li><a >No evidence for life has been found on Venus. The planet's extreme high temperatures of almost 480 degrees Celsius (900 degrees Fahrenheit) make's itan unlikely place for for life as we know it.</a></li>
                                    <li><a >Due to Venus' unique rotation, the Sun rises in the west and sets in the east.</a></li>
