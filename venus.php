                            <h2 class="headers" style="font-size: 1.4em;">Physical Characteristics</h2>
                            <div class='col-md-4'>
                                <div class='img-holder imgSquare'>
                                    <img src='images/venus2.jpg'>
                                </div>
                            </div>
                            <div class='col-md-8 blah inner2'>
                                <p>Venus is the hottest world in the solar system, despite not being the planet closest to the Sun. This is due to a runaway greenhouse effect similar to that which warms Earth. It also has a heavy atmosphere, heavier than any other planet, leading to a surface pressure 90 times  that of Earth.</p>
                                <p>The surface of Venus is extemely dry, as well. During the planet's evolution, ultraviolet rays from the Sun evaporated water, keeping the planet in a prolonged molten state. Because of the scorching heat, there is no liquid water on Venus' surface, as the atmosphere would cause any liquid to boil away. </p>
                                <p>Lastly, about two-thirds of the planet's surface is covered by flat, smooth plains that are marred by valcanoes - some of which are still active today. </p>
