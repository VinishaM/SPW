                          <h2 class="headers" style="font-size: 1.4em;">Research and Exploration</h2>
                          <div class='col-md-12 blah inner2'>
                              <p>Many missions have been deployed to Venus including NASA's Mariner 2, which came within 21,000 miles of Venus in 1962, making it the first planet to be ovserved by a passing spacecraft. The Soviet Union's Venera 7 was the first spacecraft to land on another planet, and later, Verera 9 returned the first images of Venus' surface. Over the years following this achievement, several spacecraft have orbited and passed Venus, including Venus Express, which spent 8 years orbiting Venus before running out of fuel in 2014. </p>
                              
                              <p>The next mission to Venus will be Japan's Akatsuki, which was first launched in 2010 but it's main engine died and the spacecraft was sent hurling into space. It was then recreated with smaller thrusters and the Japanese team hopes to send it into orbit in November 2015. </p>
