                            <div class='col-sm-12 blah inner2'>
                                <p>Kepler-37d is the largest of three planets that orbit the star Kepler-37. It is located about 200 light years away and was discovered by the Kepler mission in the constellation Lyra, along with other exoplanets Kepler-37b and Kepler-37c. It has an orbital period of 40 days.</p>
                                
                                <p>The Kepler mission was launched in 2009 and orbits the Sun every 371 days. As it travels, Kepler remains pointed at a single patch of sky. Sensors monitor the brightness of more than 150,000 stars simultaneously, looking for drops in intensity that could indicate an orbiting planet passing in front of the star, thereby blocking out light emanating from the star. By monitoring the same stars, it is possible to keep track of how long it takes for a planet to orbit its star and block the same amount of light again.</p>
                                
                                <p>There is a relationship between the orbital period of the planet around its home star and the distance a planet is from its star, according to Kepler's Third Law. Therefore, scientists are able to determine the distance each planet is from its star, and what its diameter must be in order to block out a specific amount of light. </p>
